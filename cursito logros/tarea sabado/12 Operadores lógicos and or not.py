#Ejercicio 12: Operadores lógicos (and, or, not)

#Escribe un programa que demuestre el uso de los operadores lógicos and, or y not. 
# Puedes crear dos variables booleanas con valores predefinidos (True o False) y mostrar el resultado de aplicar estos operadores.

es_estudiante = True
es_desempleado = False

y = es_estudiante and es_desempleado
print(y)

o = es_estudiante or es_desempleado
print(o)

n = not (es_desempleado)
print(n)